// Declarar o primeiro número
const numA = 10;

// Declarar o segundo número
const numB = 5;

// Adição
const resultadoSoma = numA + numB;
console.log(`Resultado da soma entre ${numA} e ${numB} é ${resultadoSoma}`);

// Subtração
const resultadoSubtracao = numA - numB;
console.log(`Resultado da subtração entre ${numA} e ${numB} é ${resultadoSubtracao}`);

// Multiplicação
const resultadoMultiplicacao = numA * numB;
console.log(`Resultado da multiplicação entre ${numA} e ${numB} é ${resultadoMultiplicacao}`);

// Divisão
const resultadoDivisao = numA / numB;
console.log(`Resultado da divisão entre ${numA} e ${numB} é ${resultadoDivisao}`);